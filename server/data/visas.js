export function findVisa(code) {
  return visas.find((v) => v.code === code);
}

export function filterVisas({ location, category, q } = {}) {
  let result = visas;
  if (location) result = result.filter((v) => v.location.includes(location));
  if (category) result = result.filter((v) => v.category === category);
  if (q) {
    const needle = q.toLowerCase();
    result = result.filter((v) =>
      [v.code, v.name, v.summary, v.whoFor, v.category, ...(v.tags || [])]
        .join(' ')
        .toLowerCase()
        .includes(needle)
    );
  }
  return result;
}
